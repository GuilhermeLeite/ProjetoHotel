    private int idSort(List<String> l){
        int id;
        ArrayList<String> s = new ArrayList<>();
        int t=0;
        String[] comp = null;
        
        while (t < l.size()) {
            comp = l.get(t).split("\\;");
            
            for(int i=2; i<comp.length; i+=2){
                    s.add(comp[i]);
            }            
            t++;
        }
        
        //System.out.println("------->>>>");
        Collections.sort(s);
        System.out.println(s);
            //System.out.println(""+Arrays.toString(comp));
            id = Integer.parseInt(""+s.get(s.size()-1));
            System.out.println("-------ID "+id);
        return id;
    }
    
    
    
    private void editButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editButtonActionPerformed
        // TODO add your handling code here:
         File f = arq.getFile();
        lista=arq.lerArquivo(f);
        Usuário.setId(idSort(lista));
        int id = Usuário.getId();
        System.out.println(""+id);
        String nu=null;
        while(nu==null||nu.equals("")){
            nu = JOptionPane.showInputDialog("Novo nome de usuário:");
        }
        String ns=null;
        while(ns==null||ns.equals("")){
            ns = JOptionPane.showInputDialog("Nova Senha");
        }
        if(!userCampo.equals("")){
            if (arq.regravarDados(f, BufferCHECK.editarUsuario(lista, userCampo.getText(), nu, ns, id+1))) {
                preencher();
                deleteButton.setEnabled(false);
                JOptionPane.showMessageDialog(this, "Usuário Alterado", "Alteração", JOptionPane.INFORMATION_MESSAGE);
                editButton.setEnabled(false);
                preencher();
            } else {
                JOptionPane.showMessageDialog(this, "Falha na Edição", "Edição", JOptionPane.ERROR_MESSAGE);
            }
        }else{
            JOptionPane.showMessageDialog(this, "clique no usuario que quer alterar","Alerta",JOptionPane.WARNING_MESSAGE);
        }
       
    }//GEN-LAST:event_editButtonActionPerformed

