    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        // TODO add your handling code here:
        
        File arquivo = arq.getFile();
        lista=arq.lerArquivo(arquivo);
        Usuário.setId(idSort(lista));
        
        String user=null;
        while(user==null||user.equals("")){
            user = JOptionPane.showInputDialog("Digite o usuário");
        }
        String pass=null;
        while(pass==null||pass.equals("")){
            pass = JOptionPane.showInputDialog("Digite a senha");
        }
        Usuário u = new Usuário(user, pass);
        if (BufferCHECK.novoUsuario(arq.lerArquivo(arquivo), u.getLogin())) {
            if (arq.novoDado(arquivo, u.getLogin() + ";" + u.getSenha()+";"+u.getCode())) {
                JOptionPane.showMessageDialog(null, "Salvo com sucesso", "Novo usuario", JOptionPane.INFORMATION_MESSAGE);
                preencher();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Falha em salvar", "Novo usuario", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_addButtonActionPerformed

    
                                  
    
    private void preencher(){
        File f = arq.getFile();
        lista=arq.lerArquivo(f);
        DefaultTableModel model = (DefaultTableModel) tabelaUsuarios.getModel();
        model.setNumRows(0);
        int t=0;
        while(t<lista.size()){
            model.addRow(lista.get(t).toString().split(";"));
            t++;
        }
        
    }
    
    
